const keys = [...'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ']
function generateString(keys, values, string) {
  let html = string.match(/<b>(.*?)<\/b>|<i>(.*?)<\/i>/g)
  if (!html) return string
  for (let i = 0; i < html.length; i++) {
    let alphabets = {}, new_array = [], array = [...html[i]]
    keys.forEach((key, i) => alphabets[key] = values[i])
    for (let index = 0; index < array.length; index++) {
      new_array.push(alphabets[array[index]] || array[index])
    }
    string = string.replace(html[i], new_array.join(''))
  }
  return string
}
